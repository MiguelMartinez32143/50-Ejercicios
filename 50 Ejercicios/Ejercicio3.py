# Programa que calcula el área de un rectángulo

base = int(input("Ingresa la base del rectángulo: "))     # Se pide al usuario la base
altura = int(input("Ingresa la altura del rectángulo: ")) # Se pide al usuario la altura

area = base * altura  # Se calcula el área multiplicando base por altura

# Se muestran los resultados de forma clara
print(f"Rectángulo de {base} x {altura}")
print(f"El área es: {area} unidades cuadradas")
